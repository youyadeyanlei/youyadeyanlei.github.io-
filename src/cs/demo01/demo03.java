package cs.demo01;

public class demo03 {
    public static void main(String[] args) {
        /*
        1. 整数类型: byte ,int, short , long 1248
2. 浮点类型: float, double 48
3. 布尔类型: boolean (定义布尔类型,取值只有两种: true和false) 1
4. 字符类型 : char   (定义字符类型的值,用单引号引起来, 单引号有且仅有一个字符)2

         */

        byte  b=1;
        System.out.println(b);

        short s=2;
        System.out.println(s);

        int i =4;
        System.out.println(i);

        long l=8;
        System.out.println(l);

        float f=1.5f;
        System.out.println(f);

        double d=12.12;
        System.out.println(d);

        boolean b1=true;
        boolean b2=false;
        System.out.println(b1);
        System.out.println(b2);

        char c='c';
        System.out.println(c);


    }
}
